package de.wusel.partyplayer.cli;

import java.io.File;

public final class TrackInfo {

    private String number;
    private String fileType;
    private String duration;
    private String artist;
    private String album;
    private String date;
    private String cddb;
    private String title;
    private String genre;
    private File file;
    private String md5;

    public File getFile() {
        return file;
    }

    public void setFile(File file) {
        this.file = file;
    }

    public String getMd5() {
        return md5;
    }

    public void setMd5(String md5) {
        this.md5 = md5;
    }

    public String getGenre() {
        return genre;
    }

    public void setGenre(String genre) {
        this.genre = genre;
    }

    public String getAlbum() {
        return album;
    }

    public void setAlbum(String album) {
        this.album = album;
    }

    public String getArtist() {
        return artist;
    }

    public void setArtist(String artist) {
        this.artist = artist;
    }

    public String getCddb() {
        return cddb;
    }

    public void setCddb(String cddb) {
        this.cddb = cddb;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getDuration() {
        return duration;
    }

    public void setDuration(String duration) {
        this.duration = duration;
    }

    public String getFileType() {
        return fileType;
    }

    public void setFileType(String fileType) {
        this.fileType = fileType;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return "TrackInfo{" + "number=" + number + ", fileType=" + fileType + ", duration=" + duration + ", artist=" + artist + ", album=" + album + ", date=" + date + ", cddb=" + cddb + ", title=" + title + ", genre=" + genre + ", fileName=" + file.getAbsolutePath() + ", md5=" + md5 + '}';
    }
    
    
}
